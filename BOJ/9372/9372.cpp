#include <bits/stdc++.h>
using namespace std;
int n, m;
int main(){
	int tc;
	scanf("%d", &tc);
	while(tc--){
		scanf("%d%d", &n, &m);
		for(int i=0;i<m;i++){
			int a, b;
			scanf("%d%d", &a, &b);
		}
		printf("%d\n", n-1);
	}
	return 0;
}

